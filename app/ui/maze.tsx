function defineComponent(row: number, col: number) {
  const cell = maze[row]?.[col];

  // Not a wall? Show empty.
  if (cell !== 1) return <EmptyCell />;

  // Check neighbors (treat 1 as wall; everything else is not)
  const N = maze[row - 1]?.[col] === 1;
  const S = maze[row + 1]?.[col] === 1;
  const W = maze[row]?.[col - 1] === 1;
  const E = maze[row]?.[col + 1] === 1;

  const count = [N, S, W, E].filter(Boolean).length;

  // 1) Straight walls (opposites connected)
  if ((N && S) && !(E || W)) {
    // Vertical
    return <DoubleWall rotation={0} />;
  }
  if ((E && W) && !(N || S)) {
    // Horizontal
    return <DoubleWall rotation={90} />;
  }

  // 2) Corners (two perpendicular neighbors)
  if (N && E && !S && !W) {
    // Corner connecting North+East
    return <DoubleCorner rotation={0} />;
  }
  if (E && S && !N && !W) {
    // Corner connecting East+South
    return <DoubleCorner rotation={90} />;
  }
  if (S && W && !N && !E) {
    // Corner connecting South+West
    return <DoubleCorner rotation={180} />;
  }
  if (W && N && !E && !S) {
    // Corner connecting West+North
    return <DoubleCorner rotation={270} />;
  }

  // 3) Fallbacks for tees/crosses/singletons
  // If it’s a T or cross, we don’t have a tee component—so prefer a straight that “passes through”.
  if ((E && W) || (N && S)) {
    // If both exist, prefer horizontal when E+W present
    if (E && W) return <DoubleWall rotation={90} />;
    return <DoubleWall rotation={0} />;
  }

  // Single neighbor → choose a straight pointing toward that neighbor
  if (N || S) return <DoubleWall rotation={0} />;
  if (E || W) return <DoubleWall rotation={90} />;

  // Isolated wall block
  return <DoubleWall rotation={0} />;
}

export default function Maze() {
  return (
    <div
      className="maze"
      style={{
        display: "grid",
        gridTemplateColumns: `repeat(${maze[0].length}, 20px)`, // 28 columns
        gridTemplateRows: `repeat(${maze.length}, 20px)`,       // 36 rows
      }}
    >
      {maze.map((row, rowIndex) =>
        row.map((_, colIndex) => (
          <div key={`${rowIndex}-${colIndex}`}>
            {defineComponent(rowIndex, colIndex)}
          </div>
        ))
      )}
    </div>
  );
}
