function defineComponent(x: number, y: number) {
    const isNorth = maze[x - 1]?.[y] === 1;
    const isSouth = maze[x + 1]?.[y] === 1;
    const isWest  = maze[x]?.[y - 1] === 1;
    const isEast  = maze[x]?.[y + 1] === 1;

    switch (true) {
    // Vertical double wall (north & south, no east/west)
    case isNorth && isSouth && !isEast && !isWest:
        return <DoubleWall rotation={0} />;

    // Corner (north, south, east, no west)
    case isNorth && isSouth && isEast && !isWest:
        return <DoubleCorner rotation={0} />;

    // Corner (north, south, east, no west)
    case isNorth && isSouth && !isEast && isWest:
        return <DoubleCorner rotation={90} />;

    // Horizontal double wall (east & west, no north/south)
    case isEast && isWest && !isNorth && !isSouth:
        return <DoubleWall rotation={90} />;

    default:
        return <EmptyCell/>;
    }
}

export default function Maze() {
  return (
    <div
      className="maze"
      style={{
        display: "grid",
        gridTemplateColumns: `repeat(${maze[0].length}, 20px)`, // 28 columns
        gridTemplateRows: `repeat(${maze.length}, 20px)`,       // 36 rows
      }}
    >
      {maze.map((row, rowIndex) =>
        row.map((_, colIndex) => (
          <div key={`${rowIndex}-${colIndex}`}>
            {defineComponent(rowIndex, colIndex)}
          </div>
        ))
      )}
    </div>
  );
}
